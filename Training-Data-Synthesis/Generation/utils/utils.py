def image_grid(imgs, rows, cols):
    assert len(imgs) == rows*cols

    w, h = imgs[0].size
    grid = Image.new('RGB', size=(cols*w, rows*h))
    grid_w, grid_h = grid.size
    
    for i, img in enumerate(imgs):
        grid.paste(img, box=(i%cols*w, i//cols*h))
    return grid

def image_legend(class_names, colors):
    # Create a legend image
    legend_width = 512
    # legend_height = len(class_names) * 
    legend_height = 512
    legend_image = Image.new('RGBA', (legend_width, legend_height), (255, 255, 255, 128))

    # Draw class names and colors on the legend image
    draw = ImageDraw.Draw(legend_image)
    # font = ImageFont.truetype("arial.ttf", 15)

    for idx, (class_name, color) in enumerate(zip(class_names, colors)):
        draw.rectangle([(10, idx * 30), (30, idx * 30 + 20)], fill=color)
        draw.text((40, idx * 30), class_name, fill='black')
        
    return legend_image
    
